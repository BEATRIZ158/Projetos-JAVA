    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        // passa para o modelo os dados necessÃ¡rios para
        // alterar no banco e depois passa o modelo preenchido
        // para o objeto est, que faz as alteraÃ§Ãµes dos dados
        modelo.setNome(jTextFieldNome.getText());
        modelo.setStatus(String.valueOf(jComboBoxStatus.getSelectedItem()));
        if(String.valueOf(jComboBoxStatus.getSelectedItem()).equals("Ativo")){
            modelo.setStatus("A");
        }else{
            modelo.setStatus("B");
        }
        modelo.setNomecidade(String.valueOf(jComboBoxCidade.getSelectedItem()));
        modelo.setCodigocliente(Integer.parseInt(jTextFieldCodigo.getText()));
        cliente.alterarCliente(modelo);
        jTableCliente.removeAll();
        preencherTabelaCliente();
    }//GEN-LAST:event_jButtonEditarActionPerformed


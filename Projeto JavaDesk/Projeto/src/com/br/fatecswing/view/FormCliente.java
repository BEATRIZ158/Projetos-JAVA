    public void preencherCombo(){
        jComboBoxCidade.removeAllItems();
        try {
            conexao.excutaSQL("select * from cidade order by nomecidade");
            conexao.resultset.first();
            do {
                jComboBoxCidade.addItem(conexao.resultset.getString("nomecidade"));
            } while (conexao.resultset.next());

            //conexao.desconetaBanco();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher a combo!\nErro: " + ex.getMessage());
            ex.getStackTrace();
        }

    }
    
    
    public void preencherTabelaCliente(){
        ArrayList dados = new ArrayList();
        // CriaÃ§Ã£o de Array para a inserÃ§Ã£o dos tÃ­tulos no cabeÃ§alho
        String[] colunas = new String[]{"CÃ³digo", "Nome", "Cidade"};
        try {
            conexao = new SingleConnection();
            conexao.conectar();
            conexao.excutaSQL("select * from cliente inner join cidade on cliente.cidade = cidade.idcidade order by cliente");
            conexao.resultset.first();
            do{
                dados.add(new Object[]{new Integer(Integer.parseInt(conexao.resultset.getString("idcliente"))), conexao.resultset.getString("nome"), conexao.resultset.getString("nomecidade")});
            }while(conexao.resultset.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "OPA! Nenhum registro cadastrado.", "AVISO", JOptionPane.WARNING_MESSAGE);
        }
        //Criando tabela
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableCliente.setModel(modelo);
        jTableCliente.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTableCliente.getColumnModel().getColumn(0).setResizable(false);
        jTableCliente.getColumnModel().getColumn(1).setPreferredWidth(450);
        jTableCliente.getColumnModel().getColumn(1).setResizable(true);
        jTableCliente.getColumnModel().getColumn(2).setPreferredWidth(200);
        jTableCliente.getColumnModel().getColumn(2).setResizable(true);
        jTableCliente.getTableHeader().setReorderingAllowed(false);
        jTableCliente.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        jTableCliente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
    }
    
    
    
